//The Promise obj represents the eventual completion or fail of an asynchronous operation and its resulting value.
//A Promise is a proxy for a value not necessarily known when the promise is created. 
//It allows you to associate handlers with an asynchronous action's eventual success or failure.
//This lets asynchronous method return values like sychronous methods, instead of immediately returning the final value, - 
// - the asynchronous method returns a promise to supply the value at some point in the future. 
//A promise is in one of these states, pending: initial state, neither fulfilled nor rejected.
//Fulfilled: meaning the operation was completed successfully. 
//Rejected: meaning the operation failed. 